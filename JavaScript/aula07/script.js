function calc(){
    if(op.value == "+")
        result.value = parseInt(v1.value) + parseInt(v2.value);
    else if(op.value == "-")
        result.value = parseInt(v1.value) - parseInt(v2.value);
    else if(op.value == "*")
        result.value = parseInt(v1.value) * parseInt(v2.value);
    else if(op.value == "/")
        result.value = parseInt(v1.value) / parseInt(v2.value);
}